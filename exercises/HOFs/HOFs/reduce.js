// recebe dois parametros: uma callback e o valor inicial(opcional e gerlamente é zero)
// a callback pode receber até 4 params: Acumulador: valor que vai ser acumulado a cada iteração,
// Valor atual: valor do item a ser adicionado ao acumulador,
// index do item que está sendo iterado,
// array original

const valorItens = [1, 32, 44, 2, 3];

valorItens.reduce((acc, curr) => {
  console.log("a soma atual é", acc + curr);

  return acc + curr;
}, 0);


const numbers = [50, 85, -30, 3, 15];
// retornar maior valor
const maior = numbers.reduce((acc, curr) => {
  if (acc > curr) {
    return acc;
  }
  return curr;
});
console.log(maior);


