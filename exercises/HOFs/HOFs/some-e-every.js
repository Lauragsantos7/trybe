// some:
const pessoas = [
  { nome: 'Ana', cargo: 'Analista' },
  { nome: 'João', cargo: 'Gerência' },
  { nome: 'Aline', cargo: 'Analista' },
  { nome: 'Joana', cargo: 'Gerência' },
];

// verificar se pelo menos uma pessoa tem o cargo de gerente:
const manager = pessoas.some((pessoa) => pessoa.cargo === "Gerência"); // retorna true ( e não o nome da pessoa)
console.log(manager)


// every:
const grades = {
  portugues: 'Aprovado',
  matematica: 'Reprovado',
  ingles: 'Aprovado',
};

const verifyGrades = Object.values(grades).every((grade) => grade === 'Aprovado'); // false

console.log(verifyGrades);